export function getCaseById(id: string): Case | undefined {
  return cases.find(c => c.id === id);
}

export function getSubmissionsByTask(taskId: string): TaskSubmission[] {
  return taskSubmissions.filter(s => s.taskId === taskId);
}

export function getSubmissionsByStudent(studentId: string): TaskSubmission[] {
  return taskSubmissions.filter(s => s.studentId === studentId);
}
